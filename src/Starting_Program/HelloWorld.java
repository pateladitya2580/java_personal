//✅ CamelCase
//
//first letter small, baaki har word ka first letter Capital
//
//Example: myName, totalMarks, getUserInput()
//
//Use hota hai: functions, variables, objects
//
//✅ PascalCase
//
//every word ka first letter Capital, including the first word
//
//Example: StringBuilder, Scanner, ReadChar, MyClass
//
//Use hota hai: Class names
//
//1 Line Me Sabka Formula
//
//camelCase → smallStart
//
//PascalCase → CapitalStart
//
//Java Rule (final):
//where	naming
//Class	PascalCase
//Method	camelCase
//Variable	camelCase
//Object name	camelCase